    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        // TODO add your handling code here:
        if(!inProgress){
            tripitaka_main.mainW.setVisible(true);
            tripitaka_main.resW.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(this, "A search is in progress at this time, Please stop it before close this window","Action Denied!",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_formWindowClosing

