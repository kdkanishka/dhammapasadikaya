    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (textField1.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Given keword is too short!", "Invalid Data", JOptionPane.ERROR_MESSAGE);
        } else if (!textField1.getText().equals("")) {
            if (!inProgress) {
                jLabel5.setText("");
                jPanel1.removeAll();
                jPanel1.revalidate();
                jPanel1.repaint();

                //call garbage collector before loading new search results
                System.gc();

                tcr = new TripitakaCrawler(jLabel3, jPanel1, textField1.getText(), jLabel5, jButton1);
                tcr.start();
                jButton1.setText("kj;ajkak");
                inProgress = true;
            } else {
                if (tcr.isAlive()) {
                    tcr.stop();
                    inProgress = false;
                    jButton1.setText("fidhkak");
                    jLabel3.setVisible(false);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please enter some text to search", "Invalid Data", JOptionPane.ERROR_MESSAGE);
        }
            //jScrollPane1.getVerticalScrollBar().setValue(jScrollPane1.getVerticalScrollBar().getMinimum());
            //jScrollPane1.getHorizontalScrollBar().setValue(jScrollPane1.getHorizontalScrollBar().getMinimum());
    }//GEN-LAST:event_jButton1ActionPerformed

