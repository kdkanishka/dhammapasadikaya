    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        URL url= getClass().getResource("/ico2.png");
        ImageIcon imageIcon = new ImageIcon(url);
        Image image = imageIcon.getImage();
        if (MetaData.lang == 0) {
            Keyboard_Si kb = new Keyboard_Si(null,true,textField1);
            kb.setVisible(true);
            kb.setIconImage(image);
        }else if(MetaData.lang == 2){
            Keyboard_Pali kb=new Keyboard_Pali(textField1);
            kb.setVisible(true);
            kb.setIconImage(image);
            kb.setLocationRelativeTo(null);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

